print("saya adalah programer")

# print using format
print("saya adalah {} dan saya berumur {}".format("ulia", 12))

# print using f-string
name = "ulia"
age = 12
print(f"saya adalah {name} dan saya berumur {age}")

# print using variable
name = "ulia"
age = 12
print("saya adalah", name, "dan saya berumur", age)

# print using variable and format
name = "ulia"
age = 12
print("saya adalah %s dan saya berumur %s" % (name, age))

# multiple line print
print("""saya adalah programer
    saya berumur 12 tahun
    saya tinggal di jakarta
    saya suka makan
    saya suka tidur""")

print("saya adalah programer \
    saya berumur 12 tahun \
    saya tinggal di jakarta \
    saya suka makan \
    saya suka tidur")


